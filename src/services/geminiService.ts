/// <reference types="vite/client" />
import { GoogleGenAI } from "@google/genai";
import { VaccineDefinition, ChildProfile, VaccinationProfile } from "../types";

// Lazy initialization - only create when API key is available
let ai: GoogleGenAI | null = null;

const getAIClient = (): GoogleGenAI | null => {
  if (ai) return ai;

  // Try to get API key from environment
  const apiKey = import.meta.env.VITE_API_KEY || import.meta.env.VITE_GEMINI_API_KEY;

  if (!apiKey) {
    console.warn("Gemini API key not found. AI features will be disabled.");
    return null;
  }

  try {
    ai = new GoogleGenAI({ apiKey });
    return ai;
  } catch (error) {
    console.error("Failed to initialize Gemini AI:", error);
    return null;
  }
};


