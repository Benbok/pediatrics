def main():
    db = sqlite3.connect(DB_PATH)
    c = db.cursor()
    now = datetime.now(timezone.utc).strftime('%Y-%m-%d %H:%M:%S')

    created = 0
    updated = 0

    print(f'Seeding DiagnosticTestCatalog ({len(CATALOG)} entries)...\n')

    for name_ru, typ, aliases in CATALOG:
        aliases_json = json.dumps(aliases, ensure_ascii=False)
        c.execute('SELECT id FROM diagnostic_test_catalog WHERE name_ru = ?', (name_ru,))
        row = c.fetchone()

        if row:
            c.execute('''
                UPDATE diagnostic_test_catalog
                SET type = ?, aliases = ?, is_standard = 1, updated_at = ?
                WHERE name_ru = ?
            ''', (typ, aliases_json, now, name_ru))
            print(f'  ~ updated: {name_ru}')
            updated += 1
        else:
            c.execute('''
                INSERT INTO diagnostic_test_catalog
                    (name_ru, type, aliases, is_standard, created_at, updated_at)
                VALUES (?, ?, ?, 1, ?, ?)
            ''', (name_ru, typ, aliases_json, now, now))
            print(f'  + created: {name_ru}')
            created += 1

    db.commit()
    db.close()
    print(f'\nDone. Created: {created}, Updated: {updated}')


if __name__ == '__main__':
    main()
